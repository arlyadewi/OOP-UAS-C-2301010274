    private void cSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cSimpanActionPerformed
       if (cSimpan.getText().equals("Baru")) {
    clearform();
    fieldAktif(true);

    txstatus.setVisible(false);
    Jstatus.setVisible(true);

    cSimpan.setText("Simpan"); // GANTI INI!
    cKeluar.setText("Batal");
    cUbah.setEnabled(false);
    cHapus.setEnabled(false);
} else {
    // Validasi format deadline sebelum simpan
    String deadlineInput = txdeadline.getText();
    if (!deadlineInput.matches("\\d{4}-\\d{2}-\\d{2}")) {
        JOptionPane.showMessageDialog(this, "Format deadline salah. Gunakan format YYYY-MM-DD.");
        return; // Stop proses simpan
    }

    try {
        storeData();
        lsdttugas();
    } catch (SQLException ex) {
        Logger.getLogger(TugasSekolahFiks.class.getName()).log(Level.SEVERE, null, ex);
    }
    
    cSimpan.setText("Baru");
    cKeluar.setText("Tutup");
    clearform();
    fieldAktif(false);

    txstatus.setVisible(true);
    Jstatus.setVisible(false);
}
    }//GEN-LAST:event_cSimpanActionPerformed

