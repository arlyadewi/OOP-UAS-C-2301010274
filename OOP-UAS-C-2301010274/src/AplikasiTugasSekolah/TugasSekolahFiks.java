    private void storeData() throws SQLException {
    Connection cnn = koneksi();
    String id = txid.getText(); // ambil ID dari input
    String mapel = txmapel.getText();
    String deskripsi = txdes.getText();
    String deadline = txdeadline.getText();
    String status = String.valueOf(Jstatus.getSelectedItem());

    if (!cnn.isClosed()) {
        PreparedStatement PS = cnn.prepareStatement(
            "INSERT INTO tugas (id, Mapel, Deskripsi, Deadline, Status) VALUES (?, ?, ?, ?, ?);"
        );
        PS.setString(1, id);
        PS.setString(2, mapel);
        PS.setString(3, deskripsi);
        PS.setString(4, deadline);
        PS.setString(5, status);
        PS.executeUpdate();
        cnn.close();
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
    }
}

private void updateData() throws SQLException {
    Connection cnn = koneksi();
    String id = txid.getText();
    String mapel = txmapel.getText();
    String deskripsi = txdes.getText();
    String deadline = txdeadline.getText();
    String status = String.valueOf(Jstatus.getSelectedItem());

    if (!cnn.isClosed()) {
        PreparedStatement PS = cnn.prepareStatement(
            "UPDATE tugas SET Mapel = ?, Deskripsi = ?, Deadline = ?, Status = ? WHERE id = ?;"
        );
        PS.setString(1, mapel);
        PS.setString(2, deskripsi);
        PS.setString(3, deadline);
        PS.setString(4, status);
        PS.setString(5, id); // WHERE id = ?
        int rows = PS.executeUpdate();
        cnn.close();

        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
        } else {
            JOptionPane.showMessageDialog(this, "Data gagal diubah. ID tidak ditemukan.");
        }
    }
}

    private void destroyData() throws SQLException {
        Connection cnn = koneksi();
        String id = txid.getText();

        if (!cnn.isClosed()) {
            PreparedStatement PS = cnn.prepareStatement("DELETE FROM tugas WHERE id = ?;");
            PS.setString(1, id);
            int rows = PS.executeUpdate();
            cnn.close();

            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal dihapus. ID tidak ditemukan.");
            }
        }
    }
